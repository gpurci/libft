#include "libft.h"

void				ft_lst_print(t_list *tmp)
{
	while (tmp)
	{
		write(1, "$", 1);
		write(1, tmp->content, tmp->content_size);
		write(1, "$\n", 2);
		tmp = tmp->next;
	}
}
