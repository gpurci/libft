#include "libft.h"

int					ft_count_char(char *src, int c)
{
	int				k;

	k = 0;
	while (*src)
	{
		if ((src = ft_strchr(src, c)))
			k++;
		else
			return (k);
		src++;
	}
	return (k);
}
