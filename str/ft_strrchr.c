#include "libft.h"

char			*ft_strrchr(const char *s, int c)
{
	char		*i;

	i = (char*)s - 1;
	while (*s != '\0')
		s++;
	while (i < s)
	{
		if (*s == c)
			return ((char*)s);
		s--;
	}
	return (0);
}
